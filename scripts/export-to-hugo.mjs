async function exists(file) {
  try {
    await fs.access(file);
    return true;
  } catch {
    return false;
  }
}

async function listFiles(dir, filter = () => true) {
  const entries = await fs.readdir(dir, { withFileTypes: true }).catch(() => []);
  const files = [];
  for (const entry of entries) {
    const full = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      if (entry.name === ".vuepress" || entry.name === "node_modules") continue;
      files.push(...await listFiles(full, filter));
    } else if (filter(full)) {
      files.push(full);
    }
  }
  return files;
}

function parseFrontmatter(source) {
  if (!source.startsWith("---")) return { data: {}, body: source };
  const end = source.indexOf("\n---", 3);
  if (end < 0) return { data: {}, body: source };
  const raw = source.slice(3, end).trim();
  const body = source.slice(end + 4).replace(/^\r?\n/u, "");
  return { data: YAML.parse(raw) || {}, body };
}

function stringifyFrontmatter(data) {
  return `---\n${YAML.stringify(data).trim()}\n---\n\n`;
}

function extractSoloPageId(body) {
  return String(body || "").trim().match(/^<SoloPage\s+id="([^"]+)"\s*\/>\s*$/u)?.[1] || "";
}

async function readPageHtml(pageId) {
  const file = path.join(pageDataDir, `${pageId}.ts`);
  const source = await fs.readFile(file, "utf8");
  const json = source.replace(/^export\s+default\s+/u, "").replace(/;\s*$/u, "");
  return JSON.parse(json);
}

function localContentPathForUrl(url) {
  if (url === "/") return path.join(hugoContentDir, "_index.md");
  const clean = url.replace(/^\/+|\/+$/gu, "") || "index";
  const safe = clean
    .replace(/\.html$/u, "")
    .replace(/[^a-zA-Z0-9/_-]+/gu, "-")
    .replace(/\/+/gu, "/");
  return path.join(hugoContentDir, "generated", `${safe || "index"}.md`);
}

function isTutorialUrl(url = "") {
  return url === "/mysql.html"
    || url === "/springboot4.html"
    || url === "/netty.html"
    || url.startsWith("/mysql/")
    || url.startsWith("/springboot4/")
    || url.startsWith("/netty/");
}

function dateFromUrl(url) {
  const match = String(url).match(/\/articles\/(\d{4})\/(\d{2})\/(\d{2})\//u);
  return match ? `${match[1]}-${match[2]}-${match[3]}T00:00:00+08:00` : undefined;
}

function cleanTitle(title = "") {
  return String(title).replace(/\s+-\s+jackssybin 的个人博客$/u, "").trim();
}

async function write(file, content) {
  await fs.mkdir(path.dirname(file), { recursive: true });
  await fs.writeFile(file, content, "utf8");
}

function extractConstArray(source, name) {
  const startToken = `export const ${name} = `;
  const start = source.indexOf(startToken);
  if (start < 0) return [];
  const arrayStart = source.indexOf("[", start + startToken.length);
  let depth = 0;
  let inString = false;
  let escape = false;
  for (let i = arrayStart; i < source.length; i += 1) {
    const char = source[i];
    if (inString) {
      if (escape) {
        escape = false;
      } else if (char === "\\") {
        escape = true;
      } else if (char === "\"") {
        inString = false;
      }
      continue;
    }
    if (char === "\"") inString = true;
    if (char === "[") depth += 1;
    if (char === "]") {
      depth -= 1;
      if (depth === 0) return JSON.parse(source.slice(arrayStart, i + 1));
    }
  }
  return [];
}

